#include<stdio.h>
#include<conio.h>

int main()
{
	// INCREMENT OPERATOR
	
	// ++i (pre increment)
	int i=1;
	printf("%d \n", i++);   // use, then increase
	printf("%d \n", i);     
	
	printf("   \n");
	
	// i++ (post increment)
		  /* int i=1;
	     printf("%d \n", ++i);
	     printf("%d \n", i);   */
	
	
	int a=1;
	printf("%d \n", ++a);    // increase, then use
	printf("%d \n", a);

return 0;
}

