#include<stdio.h>
#include<conio.h>

int main()
{
	// DECREMENT OPERATOR
	
	// --i (pre decrement)
	int i=1;
	printf("%d \n", i--);   // use, then decrease
	printf("%d \n", i);     
	
	printf("   \n");
	
	// i-- (post decrement)
		  /* int i=1;
	     printf("%d \n", --i);
	     printf("%d \n", i);   */
	
	
	int a=1;
	printf("%d \n", --a);    // decrease, then use
	printf("%d \n", a);



return 0;
}

